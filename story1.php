  <img class="w3-round w3-margin-bottom" src="img/madamkuri.jpg"  alt="Random Name" style="width:70%;margin-left: 50px">
    
    <h3 class="w3-justify">‘মাদাম কুরি’ একজন মহীয়সী নারী বিজ্ঞানী । তিনি একটি নয়, দু-দুটো নোবেল পেয়েছিলেন, একটি পদার্থ বিজ্ঞানে(১৯০৩) অন্যটি রসায়নে(১৯১১)। তিনিই নোবেল বিজয়ী প্রথম নারী এবং দু’টি নোবেল বিজয়ী প্রথম ব্যক্তি। মাদাম কুরি তেজস্ক্রিয় মৌল রেডিয়াম আবিষ্কার করেছিলেন এবং তিনি মানব দেহের ওপর রেডিয়ামের প্রভাব নিয়ে গবেষণা করেছিলেন। যে রেডিয়াম তিনি নিজে আবিষ্কার করেছিলেন, তার ক্ষতিকর প্রভাবেই তিনি মৃত্যুবরণ করেন। তিনি মানুষকে ব্যথামুক্ত জীবন দিতে গিয়ে নিজেই অবর্ণণীয় ব্যাথা সহ্য করেছিলেন।</h3>

    <h3 class="w3-justify">কুরি’র এগিয়ে যাবার পথ ছিলনা সোনায় রাঙানো বরং সেই ছোটবেলা থেকেই দুঃখ-কষ্ট, অনাহারি দিন এবং অপবাদ ছিলো কুরি’র জীবনের নিত্য সঙ্গী। তবুও, বিজ্ঞানের এই ধ্রুব তারা থমকে যায়নি ; নিজের লক্ষ্যপানে ছুটে চলেছিল নিরন্তর এবং নিজেকে নিয়ে গিয়েছিলেন অনন্য উচ্চতায়। আজকের লেখাটি বিজ্ঞানের এই ধ্রুব তারা’র জানা-অজনা বিষয় নিয়েই-</h3>

    <h4 class="w3-justify">১. মাদাম কুরি মেয়ে শিক্ষার্থী হওয়ায় কোন নিয়মিত শিক্ষা প্রতিষ্ঠানে (বিশ্ববিদ্যালয়ে) ভর্তি হতে পারেননি (সে সময় পোল্যন্ডে মেয়েদের জন্য উচ্চশিক্ষা নিষিদ্ধ ছিলো)। তাই , তিনি গোপনে ভ্রাম্যমান বিশ্ববিদ্যালয়ে পড়ালেখা করেন।</h4>
  
<h4 class="w3-justify">২. মাদাম কুরি ফ্রান্সে পড়াকালিন সময়ে স্বল্প আয়ে , শীতকালে ঠান্ডায় ভুগে এবং ক্ষুধায় জ্ঞান হারিয়ে জীবন-যাপন করত। প্রচন্ড শীতে আগুন জ্বালানোর জন্য ম্যাচ কেনার টাকা থাকত না বলে পুরাতন জামা-কাপাড় জড়িয়ে থাকতেন সব সময়।</h4>

<h4 class="w3-justify">৩. মাদাম কুরি তরুণী বয়সে জোরাভস্কির প্রেমে পরেন কিন্তু কুরি’র পরিবার গরীব বলে জোরাভস্কির পরিবার তা মেনে নেননি। তাই সেখানেই তাদের সম্পর্কের বিচ্ছেদ ঘটে। উল্লেখ্য, জোরাভস্কি ছিলেন একজন বিখ্যাত গণিতবিদ।</h4>

<h4 class="w3-justify">৪. মাদাম কুরি বেশিরভাগ সময় কালো পোশাক পরতেন; এর অন্যতম কারণ ছিলো তিনি যাতে সেই পোশাক দীর্ঘদিন ধরে পরতে পারে এবং ল্যাবরেটরির কাজে নোংরা হলেও তা বুঝা না যায়।</h4>

<h4 class="w3-justify">৫. রয়েল ইনস্টিউট তেজস্ক্রিয়’র উপরে ভাষণ দেয়ার জন্য মাদাম কুরি ও তার স্বামী পিয়েরে কুরি কে আমান্ত্রণ জানালেও মঞ্চে কেবল পিয়েরে কুরি ভাষণ দিতে পেরেছিলেন। কেননা মাদাম কুরি নারী বলে কতৃপক্ষ তাকে ভাষণ দেয়ার অনুমতি দেননি।</h4>

<h4 class="w3-justify">৬. মাদাম কুরি কে নোবেল কমিটি প্রথমে স্বীকৃতি দিতে অস্বীকৃতি জানায়, পরে তার স্বামী পিয়েরে কুরি ও অন্যান্য বিজ্ঞানিদের প্রতিবাদে তার নাম ঘোষণা করতে বাধ্য হয় নোবেল কমিটি।</h4>

<h4 class="w3-justify">৭. মাদাম কুরি নোবেল বিজয়ী প্রথম নারী, দু’টি নোবেল বিজয়ী প্রথম ব্যক্তি এবং দু’টি ক্ষেত্রে নোবেল বিজয়ী একমাত্র নারী।</h4>
<h4 class="w3-justify">৮. মাদাম কুরি’র স্বামী পিয়েরে কুরি, মেয়ে আইরিন কুরি এবং মেয়ের জামাই ফ্রেডরিক কুরিও ছিলেন নোবেল বিজয়ী বিজ্ঞানী।</h4>
<h4 class="w3-justify">৯. নোবেল কমিটি পুরস্কার গ্রহণ করতে আমান্ত্রণ জানালে মাদাম কুরি ও তার স্বামী পিয়েরে কুরি পুরস্কার গ্রহণ করতে যেতে অস্বকৃতি জানায়; কেননা তারা দু’জনেই সে সময় গবেষণায় ব্যস্ত ছিলেন।</h4>

<h4 class="w3-justify">১০. ৯৬ পারমাণবিক সংখ্যা বিশিষ্ট মৌলের নাম কুরিয়াম এবং ৭০০০ কুরি গ্রহাণুটি রাখা হয়েছে মাদাম কুরি’র নাম অনুসারে ।</h4>

<h4 class="w3-justify">১১. মাদাম কুরি গবেষণার সময় নিজের জামার পকেটে সবসময় রেডিয়াম পূর্ণ টেস্টটিউব রাখায় এবং প্রথম বিশ্ব যুদ্বের সময় ভ্রাম্যমাণ এক্স-রে ইউটিটে কাজ করায় তেজস্ক্রিয়’র ক্ষতিকর প্রভাবে তিনি মৃত্যুবরণ করেন(১৯৩৪)।</h4>

<h4 class="w3-justify">১২. মাদাম কুরি’র মৃত্যুর পর তার গবেষণা পত্র এবং রান্নার বই সীসা বক্সে রাখা হয়। যাতে সেগুলো থেকে তেজস্ক্রিয়’র দূষনের মাত্রা বাহিরে ছড়িয়ে না যায়।</h4>
