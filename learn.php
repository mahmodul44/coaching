   <hr>
  <img class="w3-round w3-margin-bottom" src="img/learning.jpg"  alt="Random Name" style="width:70%;margin-left: 50px">
    
    <h4 class="w3-justify">প্রতিটি মানুষই কিছু না কিছু ভিন্ন ভিন্ন উপায়ে শেখে।  কেউ শুনে শুনে খুব ভালো শেখে, যখন আবার কেউ শেখে দেখে দেখে।  তবে, অধিকাংশ লোকই ‘শুনে, দেখে, হাতে-কলমে’ এই তিন স্টাইলের একটা মিলিত প্রচেষ্টাতেই ভালো শেখে।  সে যাই হোক না কেন, নিজের লার্নিং স্টাইল খুঁজে পাওয়াটা দারুণ উপকারী এবং প্রয়োজনীয়। যদি জ্ঞানের ভিত্তিটাকে সমৃদ্ধ করতে চাও, তাহলে বুঝে নেয়ার চেষ্টা করো, সেই জ্ঞান তুমি কোন উপায়ে সবচে’ ভালোভাবে অর্জন করতে পারো।</h4>

    <h3>কীভাবে আবিষ্কার করবে নিজের লার্নিং স্টাইল?</h3>
  <h3 style="color: red" class="w3-justify">১।  বিভিন্ন ধরণের লার্নিং স্টাইল নিয়ে পড়াশোনা করো:
</h3>
 <h4 class="w3-justify">নিজের লার্নিং স্টাইল আবিষ্কারের জন্য আগে বিভিন্ন ক্যাটাগরির লার্নারদের পার্থক্য বোঝা জরুরি। টপিকটা নিয়ে রিসার্চ করার জন্য তুমি প্রচুর পরিমাণে বই এবং ওয়েবসাইট খুঁজে পাবে।  ইন্টারনেটে সার্চ দাও।  নিজেই কিছু পড়াশোনা করো।</h4>
  <h3 style="color: red" class="w3-justify">২।  ভিজ্যুয়াল বা দর্শন-পদ্ধতিতে চেষ্টা করে দেখো:</h3>
<h4 class="w3-justify">রিসার্চ করার পর নিজেই এক্সপেরিমেন্ট করতে শুরু করো, তুমি visual,  auditory, verbal, physical- এই পাঁচটির মধ্যে কোন ক্যাটাগরির লার্নার।  ভিজ্যুয়াল দিয়ে শুরু করতে পারো।  অর্থাৎ, তুমি বিভিন্ন ছবি এবং দৃশ্যের মাধ্যমে শেখার চেষ্টা করবে।  তারপর-</h4>

<h5 class="w3-center">বোঝার চেষ্টা করবে ঐসব ছবি বা দৃশ্য মূল জিনিসটা বুঝতে তোমাকে সাহায্য করেছে কিনা। হয়তো জীববিজ্ঞান পড়ছো। তাহলে চিন্তা করে দেখ, টেক্সট না ছবিগুলো তোমাকে বেশি সাহায্য করছে।যে টপিকটা পড়ছো, সেটাতে আরও বেশি বেশি ইমেজ যোগ করো।  ইতিহাসের পরীক্ষার জন্য পড়ছো? তাহলে মূল বিষয়গুলোর ওপর ভিত্তি করে চার্ট তৈরি করে ফেলো।  যদি মনে করো সেই চার্ট তোমাকে টেক্সট-এর চেয়ে বেশি সাহায্য করছে, তাহলে তুমি একজন ভিজ্যুয়াল লার্নার।</h5>

 <h3 style="color: red" class="w3-justify">৩।  অডিটরি বা শ্রবণ-পদ্ধতিতে চেষ্টা করো:</h3>
<h4 class="w3-justify">অডিটরি পদ্ধতিতে তুমি ভালো শিখতে পারো, মানে হচ্ছে, তুমি মিউজিক বা শব্দ শোনার মাধ্যমে ভালো শিখতে পারো। যদি নিশ্চিত না হও, এক্সপেরিমেন্ট করে দেখ।</h4>

<h5 class="w3-center">বইয়ের কিছু নির্বাচিত পৃষ্ঠা জোরে জোরে পড়ে দেখ সেটা কেমন ফলপ্রসূ হয়।  যদি এই পদ্ধতিতে তুমি ভালো শিখতে পারো, তবে তুমি একজন অডিটরি লার্নার।  যে যাই বলুক, জোরে জোরেই চালাও পড়ার গাড়ি।
মূল কনসেপ্টগুলো যদি পাশের কারো কাছে রিপিট করতে থাকো বা পড়ার মধ্যে যদি কথা বলে বলে বোঝার চেষ্টা করো, তাহলেও তুমি অডিটরি লার্নার।  তুমি টেক্সট-এর মধ্যে ছবি বা ডায়াগ্রাম দেখে প্রায়ই বিরক্ত হও? তাহলেও একই ব্যাপার।</h5>

<h3 style="color: red" class="w3-justify">৪।  শব্দ প্রয়োগের মাধ্যমে শেখো:</h3>
<h4 class="w3-justify">Verbal  লার্নাররা সাধারণত শব্দের মাধ্যমে শেখে, পড়ে এবং লিখে-দু’ রকমেই।  তথ্য আত্মস্থ করার জন্য বই দেখে দেখে পড়াই যদি হয় তোমার সর্বোত্তম উপায়, তাহলে তুমি একজন ভার্বাল লার্নার।</h4>

<h5 class="w3-center">তুমি কি পড়ার সময় প্রায়ই লেখো? ভার্বাল লার্নারদের কাজ হলো কাগজে-কলমে শেখার বিষয়বস্তুর আউটলাইন তৈরি করা।
বন্ধু চলে যাওয়ার সময় কি বলো, “আচ্ছা, এটা নিয়ে পরে আমরা আবার কথা বলব?” এটাও কিন্তু কথার মাধ্যমে শেখার কথাই নির্দেশ করছে।  অর্থাৎ, তুমি এভাবেই শিখতে মজা পাও।  তোমার লার্নিং স্টাইল ভার্বাল।</h5>

<h3 style="color: red" class="w3-justify">৫।  মুভমেন্ট পদ্ধতিতে শেখো:</h3>
<h4 class="w3-justify">লার্নিং স্টাইলগুলোর মধ্যে ফিজিক্যাল লার্নিং কমন একটি স্টাইল।  অনেকেই দেখবে পড়ার সময় হাত-পা নেড়ে, মাথা ঝুঁকিয়ে, পেছনে ঠেলে, হেঁটে হেঁটে শেখার চেষ্টা করে। অর্থাৎ, ফিজিক্যাল মুভমেন্ট ছাড়া পড়তে পারে না। তুমিও যদি একই রকম হও, তাহলে তুমি ফিজিক্যাল লার্নার। নিশ্চিত হওয়ার জন্য পরীক্ষা করে দেখতে পারো, হলভরা দর্শকের মধ্যে নীরবে বসে বসে কিছু শিখতে তোমার কেমন বোধ হয়। যদি খুব বিরক্ত লাগে, তাহলে তুমি অবশ্যই ফিজিক্যাল লার্নার। ফ্ল্যাশ কার্ডের মাধ্যমে শেখার চেষ্টা করে দেখো তো কেমন ফল পাও? ওভাবে শেখার মধ্যে কিছু ফিজিক্যাল মুভমেন্ট-এর ব্যাপার আছে।

সবগুলো পরীক্ষা শেষে বুঝে নাও, কোন পদ্ধতিতে তোমার পড়া আত্মস্থ করতে সবচে’ সহজ। সেটাই প্রধান হিসেবে বেছে নাও।  হ্যাপি লার্নিং!</h4>
